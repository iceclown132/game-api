package org.spartan.model.user.attribute;

/**
 * The detail which the client is run in (Low or High)
 * 
 * @author brock
 *
 */
public enum Detail {
	
	/**
	 * Disables music and a lot of extra scenic objects.
	 */
	LOW,
	
	/**
	 * Enables scenery objects (such as small stones) and music
	 */
	HIGH;

}
